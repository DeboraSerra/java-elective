public class PrimitivesTypes {
    public static void main(String[] args) {
        float numA = 10.5f;
        double numB = 10.51;
        double numC = 10.51d;
        System.out.println(numA - numB);
        System.out.println(numA + numC);
        System.out.println(numB + numC);

        char letterC = 'c';
        char alsoLetterC = 99;
        System.out.println(letterC);
        System.out.println(alsoLetterC);
    }
}
